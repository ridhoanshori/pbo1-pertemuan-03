package Operator;

public class OperatorEquality {
    public static void main(String[] args) {
        int i = 9 ;
        int j = 7 ;

        System.out.println(i==9);
        System.out.println(i==8);
        System.out.println(i==j);
        System.out.println(i>j);
        System.out.println(i<j);
        System.out.println(9>7);
        System.out.println(9<7);
        System.out.println(i!=7);
        System.out.println(j!=7);
    }
}

package Percabangan;

public class Switch {
    public static void main(String[] args) {
        int jalanJalan=3;
        switch (jalanJalan){
            case 1:
                System.out.println("Fahri Tampan");
                break;
            case 2:
                System.out.println("Fahri Ganteng");
                break;
            case 3:
                System.out.println("Fahri Bungas");
                break;
            case 4:
                System.out.println("Fahri Cool");
                break;
            default:
                System.out.println("Fahri Guring");
                break;
        }
    }
}
